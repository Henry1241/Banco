    public void cargar() {
        List<Movimiento> movimiento = MovimientoDAO.obtenerTodos();
        DefaultTableModel model = (DefaultTableModel) tblMovimiento.getModel();
        model.setRowCount(0);
        for (Movimiento m : movimiento) {
            model.addRow(new Object[]{
                m.getId(),
                m.getDescripcion(),
                m.getCargo(),
                m.getFecha(),
                m.getTipo(),
                m.getIntereses()

            });
        }
    }

