    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String descripcion = txtDescripcion.getText();
            double cargo = Double.parseDouble(txtCargo.getText());
            Date fecha = new SimpleDateFormat("yyyy MM, dd", Locale.ENGLISH).parse(txtFecha.getText());
            EstadoCuenta cuenta = (EstadoCuenta) cmbEstado.getSelectedItem();
            TipoMovimiento tipo = (TipoMovimiento) cmbTipo.getSelectedItem();
            
            boolean resultado = this.id == 0 ?
                    
                    MovimientoDAO.guardar(descripcion, cargo, fecha, cuenta, tipo):
                    MovimientoDAO.guardar(descripcion, cargo, fecha, cuenta, tipo);
            if(resultado){
            JOptionPane.showMessageDialog(this, "El registro se guardó correctamente", "Registro guardado", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar", "Error", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_btnGuardarActionPerformed

