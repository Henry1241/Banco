    public void cargarCuentas() {
        List<EstadoCuenta> cuentas = EstadoCuentaDAO.obtenerTodos();
        for(EstadoCuenta c: cuentas){
            cmbEstado.addItem(c);
        }
    }
    public void cargarMovimientos() {
        TipoMovimiento[] tipo = TipoMovimiento.values();
        for(TipoMovimiento t: tipo){
            cmbTipo.addItem(t);
        }
    }

